    AND 수강.과목번호 = 과목.과목번호
    AND 학생명 = "이홍근";
    
-- 방법 2)
SELECT 학생명, 과목명, 성적
FROM 학생 INNER JOIN 수강
  ON 학생.학번 = 수강.학번
     INNER JOIN 과목
  ON 수강.과목번호 = 과목.과목번호
  WHERE 학생명 = "이홍근";